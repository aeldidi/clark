static unsigned char masks[2][8] = {
	{ 0x3f, 0x3f, 0x3f, 0x3f, 0x3f, 0x3f, 0x3f, 0x3f },
	{ 0x7f, 0x1f, 0x0f, 0x0f, 0x0f, 0x07, 0x07, 0x07 }
};

static int utf8_state_advance(const int state, uint32_t *codep,
			      const uint8_t byte)
{
	int next = utf8_table[state][byte];
	*codep = (*codep << 6) | ((int)byte & masks[!state][next & 7]);
	return next;
}

size_t utf8_codepoint_size(const uint32_t cp)
{
	if (cp <= 0x7F) {
		// ASCII
		return 1;
	} else if (cp <= 0x07FF) {
		// Two byte character
		return 2;
	} else if (cp <= 0xFFFF) {
		// Three byte character
		return 3;
	} else if (cp <= 0x10FFFF) {
		// Four byte character
		return 4;
	}

	return UTF8_ERROR;
}

bool utf8_codepoint_valid(const uint32_t c)
{
	// ranges from Table 3-7 in the Unicode 13.0.0 standard, page 124.
	return c <= 0x007f || (c >= 0x0080 && c <= 0x07ff) ||
	       (c >= 0x000800 && c <= 0x000fff) ||
	       (c >= 0x001000 && c <= 0x00cfff) ||
	       (c >= 0x00d000 && c <= 0x00d7ff) ||
	       (c >= 0x00e000 && c <= 0x00ffff) ||
	       (c >= 0x010000 && c <= 0x03ffff) ||
	       (c >= 0x040000 && c <= 0x0fffff) ||
	       (c >= 0x100000 && c <= 0x10ffff);
}

void utf8_codepoint_encode(const uint32_t cp, const size_t out_len,
			   uint8_t *out)
{
	if (out == NULL || out_len == 0) {
		panic("utf8_codepoint_encode: out == NULL || out_len == 0");
	}

	size_t size = utf8_codepoint_size(cp);
	if (out_len < size) {
		panic("couldn't encode codepoint U+%.4X: buffer too small", cp);
	}

	uint8_t *bytes = (uint8_t *)out;

	switch (size) {
	case 1:
		bytes[0] = (uint8_t)cp;
		break;
	case 2:
		bytes[0] = ((cp >> 6) & 0x1f) | 0xc0;
		bytes[1] = ((cp >> 0) & 0x3f) | 0x80;
		break;
	case 3:
		bytes[0] = ((cp >> 12) & 0x0f) | 0xe0;
		bytes[1] = ((cp >> 6) & 0x3f) | 0x80;
		bytes[2] = ((cp >> 0) & 0x3f) | 0x80;
		break;
	case 4:
		bytes[0] = ((cp >> 18) & 0x07) | 0xf0;
		bytes[1] = ((cp >> 12) & 0x3f) | 0x80;
		bytes[2] = ((cp >> 6) & 0x3f) | 0x80;
		bytes[3] = ((cp >> 0) & 0x3f) | 0x80;
		break;
	default:
		panic("couldn't encode codepoint U+%.4X: invalid", cp);
	}

	return;
}

uint32_t utf8_codepoint_decode(const size_t str_len, const uint8_t *str,
			       const size_t current, size_t *size)
{
	if (current >= str_len) {
		*size = 0;
		return UTF8_EOF;
	}

	if (str == NULL || str_len == 0 || size == NULL) {
		panic("utf8_codepoint_decode: str == NULL || str_len == 0 || "
		      "size == NULL");
	}

	const uint8_t *ptr = str + current;
	uint32_t result = 0;
	int state = 0;
	*size = 0;
	for (size_t i = 0; i < str_len; i += 1) {
		state = utf8_state_advance(state, &result, ptr[i]);
		*size += 1;
		if (state == UTF8_REJECT || state == UTF8_ACCEPT) {
			break;
		}
	}

	if (state != UTF8_ACCEPT) {
		*size = 1;
		return UTF8_ERROR;
	}

	return result;
}
